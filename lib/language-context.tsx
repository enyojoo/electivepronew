// Helper function to detect browser language
const detectBrowserLanguage = (): "en" | "ru" => {
  if (typeof window === "undefined") return "en" // Default for SSR

  const browserLang = navigator.language.toLowerCase().split("-")[0]
  return browserLang === "ru" ? "ru" : "en"
}

// Helper function to get stored language preference
const getStoredLanguage = (): "en" | "ru" | null => {
  if (typeof window === "undefined") return null // Return null for SSR

  try {
    const storedLang = localStorage.getItem(LANGUAGE_STORAGE_KEY)
    return storedLang === "ru" ? "ru" : storedLang === "en" ? "en" : null
  } catch (error) {
    console.error("Error accessing localStorage:", error)
    return null
  }
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  // Initialize language synchronously on client to avoid flash
  // Check stored preference first, then fall back to browser language
  const [language, setLanguageState] = useState<"en" | "ru">(() => {
    // Only run on client side
    if (typeof window === "undefined") return "en"
    
    const storedLang = getStoredLanguage()
    if (storedLang) return storedLang
    
    return detectBrowserLanguage()
  })

  // Custom setter that also updates localStorage
  const setLanguage = useCallback((newLanguage: "en" | "ru") => {
    setLanguageState(newLanguage)
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, newLanguage)
      // Dispatch custom event to notify other components (like brand context)
      if (typeof window !== "undefined") {
        window.dispatchEvent(new Event("language-changed"))
      }
    } catch (error) {
      console.error("Error setting localStorage:", error)
    }
  }, [])

  const t = useCallback(
    (key: string, params?: Record<string, any>) => {
      const dict = translations[language] || translations.en
      let translation = dict[key] || key
      
      // Support parameter substitution (e.g., {count} in translation strings)
      if (params) {
        translation = Object.entries(params).reduce((acc, [paramKey, value]) => {
          // Escape special regex characters in paramKey and use literal braces
          const escapedKey = paramKey.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")
          return acc.replace(new RegExp(`\\{${escapedKey}\\}`, "g"), String(value))
        }, translation)
      }
      
      return translation
    },
    [language],
  )

  return <LanguageContext.Provider value={{ language, setLanguage, t }}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider")
  }
  return context
}
